void    ft_reverse(char *s)
{
	int     i;
	int     j;
	char    *b;
	int		tmp;

	i = 0;
	j = ft_strlen(s)-1;
	if (i < j)
	{
		i++;
		j--;
		tmp = *s;
		*s = *b;
		*b = tmp;
	}
}
