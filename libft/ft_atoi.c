#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int n;

	i = 0;
	n = 1;
	while (ft_isspace(*str))
		str++;
	if (ft_strncmp(str, "-2147483648", 11) == 0)
		return (-2147483648);
	if (*str == '-' || *str == '+')
	{
		n = (*str == '-') ? -1 : 1;
		str++;
	}
	while (*str && *str >= '0' && *str <= '9')
	{
		i = 10 * i + (*str - '0');
		str++;
	}
	return (n * i);
}
